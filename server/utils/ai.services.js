import axios from "axios";
import dotenv from "dotenv";
import { User } from "../models/user.models.js";
dotenv.config();

export const apiResponse = async (req, res) => {
  const { code } = req.body;
  const apiKey = process.env.GEMINI_API_KEY;

  if (!apiKey) {
    return res.status(500).json({ message: "Server configuration error: Missing API Key" });
  }


  try {
    const response = await axios.post(
      apiKey,
      {
        systemInstruction: {
          parts: [
            {
              text: `You are an expert code reviewer with 7+ years of development experience. Analyze the user's code for quality, performance bottlenecks, security vulnerabilities, and logical flaws.
      
      You MUST respond ONLY with a valid JSON array of objects. Do not wrap the JSON in markdown code blocks.
      
      Each issue object inside the array MUST strictly follow this JSON structural format:
      {
        "issue_id": "A unique identifier string like SEC-001, PERF-002, BUG-001",
        "severity": "Critical", "Major", "Medium", or "Minor"",
        "category": "The aspect being analyzed (e.g., Security, Performance, Error Handling, Logic, Best Practices)",
        "title": "A short, precise title summarizing the issue",
        "description": "A deep, comprehensive, Gemini-style analytical explanation detailing why this is an issue, the exact risk it poses, and how to fix it."
      }

      The final object in the array MUST be your optimization patch, which must follow this structure:
      {
        "issue_id": "REF-001",
        "severity": "Medium",
        "category": "Readability",
        "title": "Refactored Implementation Patch",
        "description": "Provide a high-level explanation of your architectural optimizations here.",
        "codeSnippet": "Provide the full, optimized, refactored clean code block string here."
      }`
            }
          ]
        },
        contents: [
          {
            parts: [{ text: `Review the following source code:\n\n${code}` }]
          }
        ],
        generationConfig: {
          responseMimeType: "application/json"
        }
      },
      {
        headers: { "Content-Type": "application/json" }
      }
    );

    const rawText = response.data.candidates?.[0]?.content?.parts?.[0]?.text;

    if (!rawText) {
      return res.status(500).json({ message: "Empty response generated by the AI engine" });
    }

    const aiAnalysis = JSON.parse(rawText);

    // push code to history in user model
  
  const pushToHistory = await User.findByIdAndUpdate(
    req.user._id,
    {
      $push: {
        history: {
          codeSnippet: code,
          analysis: aiAnalysis
        }
      }
    },
    { returnDocument: 'after' }
  );

  if(!pushToHistory) {
    return res.status(500).json({ message: "Failed to update user history" });
  }

    return res.status(200).json({ success: true, analysis: aiAnalysis });

  } catch (error) {
    console.error("Gemini response error:", error.response?.data || error.message);
    return res.status(500).json({ 
      message: "AI Processing Failed", 
      details: error.response?.data?.error?.message || error.message 
    });
  }
};

export default apiResponse;